// responses

function ListResponseSchema<T extends z.ZodTypeAny>(item: T) {
	return z
		.object({
			page: z.string().optional(),
			total: z.union([z.string(), z.number()]).optional(),
			registros: z.array(item).optional(),
		})
		.loose();
}

export const ListClientResponseSchema = ListResponseSchema(ClientSchema);

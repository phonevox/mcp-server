export const DocumentSchema = z
	.string()
	.min(1, "Document is required")
	.refine(
		(doc) =>
			/^\d{3}\.\d{3}\.\d{3}-\d{2}$/.test(doc) || // CPF
			/^\d{2}\.\d{3}\.\d{3}\/\d{4}-\d{2}$/.test(doc), // CNPJ
		{
			message: "Document must be formatted.",
		},
	);
